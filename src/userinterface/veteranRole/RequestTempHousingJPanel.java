    private void btnRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestActionPerformed
        // TODO add your handling code here:
        TempHousingRequest tr = new TempHousingRequest();
        tr.setSender(userAccount);
        tr.setStatus("Sent");
        tr.setMessage(txtMessage.getText());
        tr.setHousingSociety((String) housingSocietyComboBox.getSelectedItem());
        tr.setNumberOfPeople(Integer.parseInt((String) NumberComboBox.getSelectedItem()));
        Organization org = null;

        for (Network network : system.getNetworkList()) {
            //Step 2.a: check against each enterprise
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
//                    userAccount = enterprise.getUserAccountDirectory().authenticateUser(userName, password);

                //Step 3:check against each organization for each enterprise
                for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    if (organization instanceof TempHousingOrganization) {
                        org = organization;
                        break;
                    }

                }
            }
        }
        if (org != null) {
            org.getWorkQueue().getWorkRequestList().add(tr);
            System.out.println("Added Request to Org ");
            userAccount.getWorkQueue().getWorkRequestList().add(tr);
            System.out.println("Added Request to UserAccount ");

    }//GEN-LAST:event_btnRequestActionPerformed
    JOptionPane.showMessageDialog(null, "Request Has Been Raised. You will be notified accordingly.", "Warning", JOptionPane.WARNING_MESSAGE);
    btnRequest.setEnabled(false);
    txtMessage.setText("");
    txtMessage.setEnabled(false);
    NumberComboBox.setEnabled(false);
    housingSocietyComboBox.setEnabled(false);
            
    }
    
